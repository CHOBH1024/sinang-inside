/**
 * 날짜를 기준으로 오늘의 말씀을 반환합니다.
 * 같은 날에는 항상 같은 말씀이 표시됩니다.
 */
export const getDailyWord = (): DailyWord => {
  const today = new Date();
  const dayIndex = Math.floor(
    (today.getFullYear() * 1000 + today.getMonth() * 31 + today.getDate()) % DAILY_WORDS.length
  );
  return DAILY_WORDS[dayIndex];
};

/**
 * 랜덤 말씀을 반환합니다 (새로고침 버튼용)
 */
export const getRandomWord = (): DailyWord => {
  return DAILY_WORDS[Math.floor(Math.random() * DAILY_WORDS.length)];
};
